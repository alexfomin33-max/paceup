let currentLang = 'en';

function setLanguage(lang) {
  currentLang = lang;
  
  // Обновляем активную кнопку языка
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === lang) {
      btn.classList.add('active');
    }
  });
  
  // Обновляем все тексты с data-i18n атрибутом
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      const translation = translations[lang][key];
      // Если элемент содержит только текст (нет дочерних элементов с data-i18n), заменяем содержимое
      const hasNestedI18n = element.querySelector('[data-i18n]');
      if (!hasNestedI18n) {
        element.textContent = translation;
      } else {
        // Если есть вложенные элементы с data-i18n, заменяем только текстовое содержимое текущего элемента
        const childNodes = Array.from(element.childNodes);
        const textNodes = childNodes.filter(node => node.nodeType === Node.TEXT_NODE);
        if (textNodes.length > 0) {
          textNodes.forEach(node => {
            if (node.textContent.trim()) {
              node.textContent = '';
            }
          });
        }
      }
    }
  });
  
  // Обновляем картинки store badge в зависимости от языка
  document.querySelectorAll('img[data-store-badge]').forEach(img => {
    if (lang === 'en') {
      img.src = 'img/app_store.webp';
      img.alt = 'App Store';
    } else {
      img.src = 'img/rustore-1.png';
      img.alt = 'RuStore';
    }
  });
  
  // Сохраняем выбранный язык в localStorage
  localStorage.setItem('preferredLanguage', lang);
}

// Инициализация языка при загрузке страницы
document.addEventListener('DOMContentLoaded', () => {
  // Проверяем сохраненный язык или используем английский по умолчанию
  const savedLang = localStorage.getItem('preferredLanguage') || 'en';
  setLanguage(savedLang);
  
  // Добавляем обработчики кликов на кнопки языка
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      const lang = btn.getAttribute('data-lang');
      setLanguage(lang);
    });
  });
});
