        child: AuthShell(
          contentPadding: const EdgeInsets.only(
            bottom: 65,
            left: 40,
            right: 40,
          ),
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              // üîπ –ò—Å–ø–æ–ª—å–∑—É–µ–º –æ–±—â–∏–π –≤–∏–¥–∂–µ—Ç –¥–ª—è –≤–≤–æ–¥–∞ —Ç–µ–ª–µ—Ñ–æ–Ω–∞
